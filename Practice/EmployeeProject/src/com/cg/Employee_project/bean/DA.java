package com.cg.Employee_project.bean;

public class DA {
    String designation;
    int da;
	public DA(String designation, int da) {
		super();
		this.designation = designation;
		this.da = da;
	}
	public String getDesignation() {
		return designation;
	}
	public void setDesignation(String designation) {
		this.designation = designation;
	}
	public int getDa() {
		return da;
	}
	public void setDa(int da) {
		this.da = da;
	}
    
}
